    function deleteItem(item, url) {
        swal({
            title: "Silmek istediğine emin misin?",
            text: "Silinen veri geri getirilemez",
            icon: "warning",
            buttons: true,
            dangerMode: true,
        }).then(function (willDelete) {
            if (willDelete) {
                $.post({
                    type: 'delete',
                    data: {
                        "_token": "{{ csrf_token() }}"
                    },
                    url: url
                }).done(function (data) {
                    if (data.status === 200) {
                        $(item).parent().parent().remove();
                    } else {
                        swal("Başarısız", {
                            icon: "error",
                        });
                    }
                });
            } else {
                swal("İptal Edildi", {
                    icon: "success",
                });
            }
        });
    }
